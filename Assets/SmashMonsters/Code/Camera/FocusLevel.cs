using UnityEngine;

namespace SmashMonsters.Code.Camera
{
	public class FocusLevel : MonoBehaviour
	{
		/*----------------------------------------------------------------------------------------*
	     * Constants
	     *----------------------------------------------------------------------------------------*/
		
		/*----------------------------------------------------------------------------------------*
	     * Injects
	     *----------------------------------------------------------------------------------------*/
	
		/*----------------------------------------------------------------------------------------*
	     * Components
	     *----------------------------------------------------------------------------------------*/
	
		/*----------------------------------------------------------------------------------------*
	     * Exposed Events
	     *----------------------------------------------------------------------------------------*/
	
		/*----------------------------------------------------------------------------------------*
	     * Exposed Variables
	     *----------------------------------------------------------------------------------------*/

		[field:SerializeField]
		public Vector3 HalfBounds { get; set; } = new Vector3(20f, 15f, 15f);

		[field:SerializeField]
		public Bounds FocusBounds { get; set; }

		/*----------------------------------------------------------------------------------------*
	     * Variables
	     *----------------------------------------------------------------------------------------*/
	
		/*----------------------------------------------------------------------------------------*
	     * Events
	     *----------------------------------------------------------------------------------------*/

		private void Update()
		{
			Vector3 pos = transform.position;
			Bounds bounds = new Bounds();
			bounds.Encapsulate(new Vector3(pos.x - HalfBounds.x, pos.y - HalfBounds.y, pos.z - HalfBounds.z));
			bounds.Encapsulate(new Vector3(pos.x + HalfBounds.x, pos.y + HalfBounds.y, pos.z + HalfBounds.z));
			FocusBounds = bounds;
		}

		/*----------------------------------------------------------------------------------------*
	     * Animation Events
	     *----------------------------------------------------------------------------------------*/

		/*----------------------------------------------------------------------------------------*
	     * Methods
	     *----------------------------------------------------------------------------------------*/

		/*----------------------------------------------------------------------------------------*
	     * Utility Methods
	     *----------------------------------------------------------------------------------------*/

		/*----------------------------------------------------------------------------------------*
	     * Coroutines
	     *----------------------------------------------------------------------------------------*/

		/*----------------------------------------------------------------------------------------*
	     * Inner Classes and Delegates
	     *----------------------------------------------------------------------------------------*/

	}
}
